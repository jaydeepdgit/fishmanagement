    public boolean changeEmail() {
        boolean flag = false;
        try {
            String sql = "UPDATE manage_email SET manage_email = ?, manage_pwd = ?, manage_port = ?, manage_host = ?, manage_mobno = ? WHERE manage_id = 1";
            PreparedStatement pstLocal = DeskFrame.connMpAdmin.prepareStatement(sql);
            pstLocal.setString(1, jtxtEmail.getText()); // Email
            pstLocal.setString(2, jtxtEmailPwd.getText()); // Password
            pstLocal.setString(3, jtxtPort.getText()); // Port
            pstLocal.setString(4, jtxtHostName.getText()); // Host Name
            pstLocal.setString(5, jtxtMobileNo.getText()); // Mobile No
            if (pstLocal.executeUpdate() > 0) {
                flag = true;
            }
        } catch (Exception ex) {
            lb.printToLogFile("exception at changeEmail In Manage Email", ex);
        }
        return flag;
    }

    private void jbtnChangeEmailActionPerformedRoutine() {
        if (jtxtEmailPwd.getText().trim().equalsIgnoreCase(jtxtConfirmPwd.getText().trim())) {
            if (changeEmail()) {
                JOptionPane.showMessageDialog(this, "Email changed sucessfully", Constants.MANAGE_EMAIL_FORM_NAME, JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Email changed unsucessfull", Constants.MANAGE_EMAIL_FORM_NAME, JOptionPane.ERROR_MESSAGE);
                jtxtEmailPwd.requestFocusInWindow();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Email password and confirm password are not match.", Constants.MANAGE_EMAIL_FORM_NAME, JOptionPane.ERROR_MESSAGE);
        }
    }
