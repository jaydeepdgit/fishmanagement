    private void setData() {
        String sql = "SELECT * FROM manage_email";
        PreparedStatement psLocal = null;
        ResultSet rsLocal = null;
        try {
            psLocal = DeskFrame.connMpAdmin.prepareStatement(sql);
            rsLocal = psLocal.executeQuery();
            if(rsLocal.next()) {
                jtxtEmail.setText(rsLocal.getString("manage_email"));
                jtxtEmailPwd.setText(rsLocal.getString("manage_pwd"));
                jtxtConfirmPwd.setText(rsLocal.getString("manage_pwd"));
                jtxtPort.setText(rsLocal.getString("manage_port"));
                jtxtHostName.setText(rsLocal.getString("manage_host"));
                jtxtMobileNo.setText(rsLocal.getString("manage_mobno"));
            }
        } catch(Exception ex) {
            lb.printToLogFile("Exception at setData In Manage Email", ex);
        }
    }

